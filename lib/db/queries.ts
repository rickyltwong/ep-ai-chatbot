// This function saves a user from Auth.js to our custom User table
export async function saveUser({
  id,
  email,
}: {
  id: string;
  email: string;
}) {
  try {
    
    // Insert into custom User table
    await db.insert(user).values({
      id,
      email,
    }).onConflictDoNothing();
    
    // Ensure the user is also in the Auth.js users table
    return await db.insert(users).values({
      id,
      email,
      emailVerified: null
    }).onConflictDoNothing();
  } catch (error) {
    console.error('Failed to save user to custom User table:', error);
    throw error;
  }
}

export async function saveUserWithPassword({
  id,
  email,
  password,
  name,
}: {
  id: string;
  email: string;
  password: string;
  name?: string;
}) {
  try {
    const salt = genSaltSync(10);
    const hash = hashSync(password, salt);
    
    // Insert into the app's original user table
    await db.insert(user).values({
      id,
      email,
      password: hash,
    }).onConflictDoNothing();
    
    // Insert into the Auth.js users table to keep both in sync
    await db.insert(users).values({
      id,
      email,
      name,
      emailVerified: null
    }).onConflictDoNothing();
    
    return { success: true };
  } catch (error) {
    console.error('Failed to save user with password in database', error);
    throw error;
  }
}
